def build_need_text(need) -> str:
    """构建用于向量化的结构化文本"""
    title = need.role_wanted
    role = 'expert'
    if need.role_wanted == 'founder':
        role = "founder"
        title = None
    elif need.role_wanted == 'investor':
        role = "investor"
        title = None
    elif need.role_wanted == 'consultant':
        role = "fa"
        title = "consultant"
    elif need.role_wanted == 'resource':
        role = "corp"
        title = None

    parts = [
        f"需要的角色: {role}",
        f"需要的岗位: {title}",
        f"具体需求: {need.description}",
        f"所属行业: {need.industry}",
        f"所在城市: {need.city_reqs}",
        f"技能要求:{need.skill_reqs}"
    ]
    return " | ".join(parts)



